gradePercentage = int(input("Enter grade percentage in numerical form: "))

if gradePercentage >100 or gradePercentage <0:
    print ("X")
elif gradePercentage >=85:
    print ("A")
elif gradePercentage >70:
    print ("B")
elif gradePercentage >=55:
    print ("C")
elif gradePercentage >=40:
    print ("D")
elif gradePercentage >=25:
    print ("E")
elif gradePercentage >=0:
    print ("F")





